    private void TablaTransporteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaTransporteMouseClicked
        try {
            PreparedStatement ps;
            ResultSet rs;
            int fila = TablaTransporte.getSelectedRow();
            int id = Integer.parseInt(TablaTransporte.getValueAt(fila, 0).toString());
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("Select TRANSPORTE_ID, descripcion,tipo,seguimiento from Transporte where TRANSPORTE_ID=?");
            ps.setInt(1, id);
            rs = ps.executeQuery();

            while (rs.next()) {
                ID_Transporte.setText(String.valueOf(id));
                Descripcion.setText(rs.getString("descripcion"));
                Tipo.setText(rs.getString("tipo"));
                Seguimiento.setText(rs.getString("seguimiento"));
                
            }

       } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString(),"", JOptionPane.ERROR_MESSAGE);

        }
      
      catch (Exception e) {
         JOptionPane.showMessageDialog(null, e.toString());

        }

    }//GEN-LAST:event_TablaTransporteMouseClicked

